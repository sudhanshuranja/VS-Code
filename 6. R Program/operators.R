# Read input from the user
number1 <- readline(prompt = "Enter the first number: ") # This line gets input from the user
number2 <- readline(prompt = "Enter the second number: ") # This line gets input from the user

# Convert input values to integers
number1 <- as.integer(number1)
number2 <- as.integer(number2)

# Print the values
print(paste("The first number is:", number1))
print(paste("The second number is:", number2))


